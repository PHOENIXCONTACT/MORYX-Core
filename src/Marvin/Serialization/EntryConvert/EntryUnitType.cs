namespace Marvin.Serialization
{
    /// <summary>
    /// Enum describes the unit type of a entry
    /// </summary>
    public enum EntryUnitType
    {
        /// <summary>
        /// No unit
        /// </summary>
        None,
        /// <summary>
        /// Unit password
        /// </summary>
        Password,
        /// <summary>
        /// Unit file
        /// </summary>
        File,
        /// <summary>
        /// Unit directory
        /// </summary>
        Directory
    }
}
